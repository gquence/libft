#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	if (!f)
		return ;
	i = 0;
	while (*s++)
		i++;
	s = s - 2;
	i--;
	while (i != -1)
	{
		f(i, s);
		s--;
		i--;
	}
}
